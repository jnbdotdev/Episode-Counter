package model;

public class Anime {
	
	private int codigo;
	private String nome;
	private int temporada;;
	private int episodio;
	private String dtLanc;
	private String dtUltEp;
	private String dtProxEp;
	private String statusAnime;
	private String statusPessoal;
	private String tipo;
	
	public int getCodigo() {
		return codigo;
	}
	public String getNome() {
		return nome;
	}
	public int getTemporada() {
		return temporada;
	}
	public int getEpisodio() {
		return episodio;
	}
	public String getDtLanc() {
		return dtLanc;
	}
	public String getDtUltEp() {
		return dtUltEp;
	}
	public String getDtProxEp() {
		return dtProxEp;
	}
	public String getStatusAnime() {
		return statusAnime;
	}
	public String getStatusPessoal() {
		return statusPessoal;
	}
	public String getTipo() {
		return tipo;
	}
	
	
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setTemporada(int temporada) {
		this.temporada = temporada;
	}
	public void setEpisodio(int episodio) {
		this.episodio = episodio;
	}
	public void setDtLanc(String dtLanc) {
		this.dtLanc = dtLanc;
	}
	public void setDtUltEp(String dtUltEp) {
		this.dtUltEp = dtUltEp;
	}
	public void setDtProxEp(String dtProxEp) {
		this.dtProxEp = dtProxEp;
	}
	public void setStatusAnime(String statusAnime) {
		this.statusAnime = statusAnime;
	}
	public void setStatusPessoal(String statusPessoal) {
		this.statusPessoal = statusPessoal;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
	
}
